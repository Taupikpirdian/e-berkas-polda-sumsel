<script type="text/javascript">
    if ("{{Session::get('success')}}") {
        Swal.fire({
            title: 'Good job!',
            text: "{{Session::get('success')}}",
            icon: 'success',
            confirmButtonColor: "#f46a6a",
            cancelButtonColor: "#3051d3"
        })
    }

    if ("{{Session::get('error')}}") {
        Swal.fire({
            title: 'Sorry!',
            text: "{{Session::get('error')}}",
            icon: 'error',
            confirmButtonColor: "#f46a6a",
            cancelButtonColor: "#3051d3"
        })
    }

    if ("{{Session::get('redirect-sweat')}}") {
        Swal.fire({
            title: 'Info!',
            text: "{{Session::get('redirect-sweat')}}",
            icon: 'warning',
            confirmButtonColor: "#f46a6a",
            showCloseButton: true,
            focusConfirm: false,
            confirmButtonText: '<a href="/profiles" style="color:#fff"> Lengkapi</a>',
        })
    }

    function authPinNotif(perkara_id, notif_type) {
        Swal.fire({
            title: 'Masukan PIN!',
            text: "Masukan PIN Anda untuk bisa melihat detail data prapenuntutan!",
            icon: 'warning',
            input: 'password',
            inputAttributes: {
                required: true,
                placeholder: 'Masukan PIN Anda',
                autocapitalize: 'off',
                maxlength: 6,
                autocorrect: 'off'
            },
            showCancelButton: true,
            confirmButtonColor: '#3085d6',
            cancelButtonColor: '#d33',
            confirmButtonText: 'Submit',
            confirmButtonClass: 'btn btn-primary',
            cancelButtonClass: 'btn btn-danger ml-1',
            cancelButtonText: 'Batal',
            buttonsStyling: false,
        }).then(function (result) {
            let pinUser = result.value;
            if (pinUser) {
                $.ajax({
                    url: "/notification/validate-pin/" + perkara_id + "/" + pinUser + "/" + notif_type,
                    method: "GET",
                    success: function (value) {
                        var data = JSON.parse(value);
                        if (data.auth_pin == true) {
                            setTimeout(function () {
                                Swal.fire({
                                    icon: data.icon,
                                    title: data.title,
                                    text: data.text,
                                    confirmButtonClass: 'btn btn-primary',
                                    buttonsStyling: false,
                                }).then(function () {
                                    if (data.url_redirect != null) {
                                        window.location = data.url_redirect;
                                    }
                                });
                            }, 1000);
                        } else {
                            setTimeout(function () {
                                Swal.fire({
                                    icon: data.icon,
                                    title: data.title,
                                    text: data.text,
                                    confirmButtonClass: 'btn btn-primary',
                                    buttonsStyling: false,
                                })
                            }, 1000);
                        }
                    }
                });
            }
        });
    }

</script>
